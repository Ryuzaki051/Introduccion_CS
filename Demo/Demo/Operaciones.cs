using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Demo
{
    /// <summary>
    /// Esta clase permite realizar operaciones matematicas
    /// </summary>
    public class Operaciones
    {
        /// <summary>
        /// Este metodo permite sumar dos numeros enteros
        /// </summary>
        /// <param name="A">Primer Operando entero</param>
        /// <param name="B">Segundo Operando entero</param>
        /// <returns>Devuelve un entero que representa la suma de A y B</returns>

        public static int Sumar(int A, int B)
        {
            return A + B;
        }
    }
}
